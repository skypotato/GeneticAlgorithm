package GA;

public class Child {
	private String str;
	private int num;

	public Child() {

		this.str = "";
		this.num = 0;
	}

	public String getStr() {
		return str;
	}

	public void setStr(String str) {
		this.str = str;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

}
